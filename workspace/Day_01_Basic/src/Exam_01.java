import java.lang.*;

// Convention (관습,관례)

/* GNU style public class~~
             	 {
             	 	 public static~~
             			 {
             			
   BSD style public class~~
   			 {
   			 	 public static~~
   			 	 {
   			 	
   K&R style public class~~ {
   			 	 public static~~ {
*/

public class Exam_01 {
	public static void main(String[] ar) {
		// Hello world를 출력하는 코드. () 안에 출력할 내용을 작성.
		System.out.println("Hello world");
		System.out.println("Java study! 한글도 되나?");
		System.out.println(10);
		System.out.println(3.14);
		System.out.println('A');
		System.out.println(10+5);
		

		
		
//	자바에서 문자열을 표현할 때는 "" 를 사용한다.
//	자바에서 한글자를 표현할 때는 '' 를 사용한다.
//	자바에서 숫자을 표현할 때는 no quote (따옴표 안씀)
	}
}